pub fn min_increment_for_unique(mut nums: Vec<i32>) -> i32 {
    nums.sort_unstable();
    let mut min = 0;
    let mut res = 0;
    for &num in nums.iter() {
        res += min.max(num) - num;
        min = (1 + min).max(1 + num);
    }
    res
}

