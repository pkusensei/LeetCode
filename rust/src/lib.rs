pub fn construct_transformed_array(nums: Vec<i32>) -> Vec<i32> {
    let n = nums.len();
    let mut res = vec![0; n];
    for i in 0..n {
        let ti = (i as i32 + nums[i]).rem_euclid(n as i32) as usize;
        res[i] = nums[ti];
    }
    res
}

