pub fn shifting_letters(s: String, shifts: Vec<i32>) -> String {
    let mut s = s.into_bytes();
    let mut curr = 0;
    for (b, sh) in s.iter_mut().zip(shifts).rev() {
        curr = (curr + sh) % 26;
        *b = ((*b - b'a') + curr as u8) % 26 + b'a';
    }
    String::from_utf8(s).unwrap()
}

