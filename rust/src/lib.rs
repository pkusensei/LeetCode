#[allow(unused_imports)]
use helper::*;

pub fn shift_grid(mut grid: Vec<Vec<i32>>, k: i32) -> Vec<Vec<i32>> {
    let mut arr: Vec<_> = grid.iter().flatten().copied().collect();
    let k = k as usize % arr.len();
    arr.rotate_right(k);
    let mut i = 0;
    let cols = grid[0].len();
    for v in arr {
        grid[i / cols][i % cols] = v;
        i += 1
    }
    grid
}

