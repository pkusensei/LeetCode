pub fn toggle_light_bulbs(bulbs: Vec<i32>) -> Vec<i32> {
    use itertools::Itertools;
    use std::collections::HashSet;
    let mut seen = HashSet::new();
    for b in bulbs {
        if !seen.insert(b) {
            seen.remove(&b);
        }
    }
    seen.into_iter().sorted_unstable().collect()
}

