#[allow(unused_imports)]
use helper::*;

pub fn max_sum(mut nums: Vec<i32>, k: i32, mut mul: i32) -> i64 {
    nums.sort_unstable();
    let mut res = 0;
    for _ in 0..k {
        let Some(v) = nums.pop() else {
            break;
        };
        res += if mul > 0 {
            i64::from(v) * i64::from(mul)
        } else {
            i64::from(v)
        };
        mul -= 1;
    }
    res
}

