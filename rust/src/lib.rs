pub fn next_greatest_letter(letters: Vec<char>, target: char) -> char {
    let mut left = 0;
    let mut right = letters.len();
    while left < right {
        let mid = left.midpoint(right);
        if letters[mid] <= target {
            left = 1 + mid;
        } else {
            right = mid;
        }
    }
    letters.get(left).copied().unwrap_or(letters[0])
}

