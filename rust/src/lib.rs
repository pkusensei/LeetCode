pub fn car_pooling(trips: &[[i32; 3]], capacity: i32) -> bool {
    let mut arr = vec![0; 1001];
    for t in trips {
        let [num, from, to] = t[..] else {
            unreachable!()
        };
        arr[from as usize] += num;
        arr[to as usize] -= num;
    }
    let mut prefix = 0;
    for v in arr {
        prefix += v;
        if prefix > capacity {
            return false;
        }
    }
    true
}

