#[allow(unused_imports)]
use helper::*;

pub fn min_time_to_visit_all_points(points: Vec<Vec<i32>>) -> i32 {
    points
        .windows(2)
        .map(|w| {
            let x = w[0][0] - w[1][0];
            let y = w[0][1] - w[1][1];
            x.abs().max(y.abs())
        })
        .sum()
}

