pub fn count_digit_occurrences(nums: Vec<i32>, digit: i32) -> i32 {
    nums.iter()
        .map(|num| {
            num.to_string()
                .bytes()
                .filter(|b| i32::from(b - b'0') == digit)
                .count() as i32
        })
        .sum()
}

