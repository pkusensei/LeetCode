#[allow(unused_imports)]
use helper::*;

pub fn left_right_difference(nums: Vec<i32>) -> Vec<i32> {
    let sum: i32 = nums.iter().sum();
    let mut left = 0;
    let mut res = vec![];
    for &num in nums.iter() {
        let right = sum - left - num;
        res.push((left - right).abs());
        left += num;
    }
    res
}

