pub fn min_bitwise_array(nums: Vec<i32>) -> Vec<i32> {
    nums.iter().map(|&num| f(num)).collect()
}

fn f(num: i32) -> i32 {
    let mut res = -1;
    let mut bit = 1;
    // e.g 23 == 0b10111
    // find this bit ^
    while num & bit > 0 {
        res = num - bit;
        bit <<= 1;
    }
    res
}

