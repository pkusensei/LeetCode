#[allow(unused_imports)]
use helper::*;

pub fn max_profit(prices: Vec<i32>, fee: i32) -> i32 {
    let mut buy = -prices[0];
    let mut sell = 0;
    for price in &prices[1..] {
        let temp = buy;
        buy = buy.max(sell - price);
        sell = sell.max(temp + price - fee)
    }
    buy.max(sell)
}

