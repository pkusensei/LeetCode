pub fn min_cost_climbing_stairs(cost: Vec<i32>) -> i32 {
    let n = cost.len();
    let mut dp = vec![i32::MAX; 1 + n];
    dp[..2].fill(0);
    for i in 0..n {
        for d in 1..=2 {
            if i + d <= n {
                dp[i + d] = dp[i + d].min(cost[i] + dp[i]);
            }
        }
    }
    dp[n]
}

