pub fn min_deletion_size(strs: Vec<String>) -> i32 {
    let n = strs[0].len();
    let mut res = 0;
    'out: for c in 0..n {
        for r in 1..strs.len() {
            if strs[r - 1].as_bytes()[c] > strs[r].as_bytes()[c] {
                res += 1;
                continue 'out;
            }
        }
    }
    res
}

