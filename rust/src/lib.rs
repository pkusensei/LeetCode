#[allow(unused_imports)]
use helper::*;

pub fn check_ones_segment(s: String) -> bool {
    let mut latest = None;
    for (i, b) in s.bytes().enumerate() {
        if b == b'1' {
            let prev = latest.get_or_insert(i);
            if *prev + 1 < i {
                return false;
            }
            *prev = i;
        }
    }
    true
}

