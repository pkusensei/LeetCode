pub const fn total_money(n: i32) -> i32 {
    const UNIT: i32 = (1 + 7) * 7 / 2;
    let div = n / 7;
    div * UNIT + (div - 1) * div / 2 * 7 + (1..=n % 7).map(|v| v + div).sum::<i32>()
}

