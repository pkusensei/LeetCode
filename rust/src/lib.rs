pub fn num_sub(s: String) -> i32 {
    const M: i32 = 1_000_000_007;
    let mut res = 0;
    let mut curr = 0;
    for b in s.bytes() {
        if b == b'1' {
            curr = (1 + curr) % M;
        } else {
            curr = 0;
        }
        res = (res + curr) % M;
    }
    res
}

