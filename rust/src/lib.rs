pub fn plus_one(digits: Vec<i32>) -> Vec<i32> {
    let mut res = vec![];
    let mut carry = 1;
    for d in digits.iter().rev() {
        let curr = (d + carry) % 10;
        carry = (d + carry) / 10;
        res.push(curr);
    }
    if carry > 0 {
        res.push(carry);
    }
    res.reverse();
    res
}

