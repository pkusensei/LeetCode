pub fn centered_subarrays(nums: Vec<i32>) -> i32 {
    use std::collections::HashSet;
    let mut res = 0;
    for (a, &_v1) in nums.iter().enumerate() {
        let mut sum = 0;
        let mut seen = HashSet::new();
        for &v2 in nums.iter().skip(a) {
            sum += v2;
            seen.insert(v2);
            res += i32::from(seen.contains(&sum));
        }
    }
    res
}

