pub fn maximum_happiness_sum(mut happiness: Vec<i32>, k: i32) -> i64 {
    happiness.sort_unstable_by(|a, b| b.cmp(a));
    happiness
        .iter()
        .zip(0..k)
        .map(|(a, b)| i64::from((a - b).max(0)))
        .sum()
}

