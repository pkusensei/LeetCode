pub fn orderly_queue(s: String, k: i32) -> String {
    let mut s = s.into_bytes();
    if k > 1 {
        s.sort_unstable();
    } else {
        let mut res = s.clone();
        for _ in 0..s.len() {
            s.rotate_left(1);
            res = res.min(s.clone());
        }
        s = res;
    }
    String::from_utf8(s).unwrap()
}

