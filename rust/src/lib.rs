#[allow(unused_imports)]
use helper::*;

pub fn longest_string(x: i32, y: i32, z: i32) -> i32 {
    if x == y {
        2 * (x + y + z)
    } else {
        2 * (2 * x.min(y) + 1 + z)
    }
}

