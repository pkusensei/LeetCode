#[allow(unused_imports)]
use helper::*;

pub fn is_middle_element_unique(nums: Vec<i32>) -> bool {
    let n = nums.len();
    let mid = nums[n / 2];
    let mut seen = false;
    for &num in nums.iter() {
        if num == mid {
            if seen {
                return false;
            }
            seen = true
        }
    }
    true
}

