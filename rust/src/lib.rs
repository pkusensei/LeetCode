pub fn find_final_value(nums: Vec<i32>, original: i32) -> i32 {
    use std::collections::HashSet;
    let set: HashSet<_> = nums.into_iter().collect();
    let mut x = original;
    while set.contains(&x) {
        x *= 2;
    }
    x
}

