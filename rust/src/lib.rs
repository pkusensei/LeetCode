#[allow(unused_imports)]
use helper::*;

pub fn find_final_value(nums: Vec<i32>, original: i32) -> i32 {
    let set: std::collections::HashSet<_> = nums.into_iter().collect();
    let mut res = original;
    while set.contains(&res) {
        res *= 2;
    }
    res
}

