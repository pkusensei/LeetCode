#[allow(unused_imports)]
use helper::*;

pub fn min_start_value(nums: &[i32]) -> i32 {
    let mut res = 1;
    let mut prefix = 0;
    for &num in nums {
        prefix += num;
        res = res.max(1 - prefix);
    }
    res
}

