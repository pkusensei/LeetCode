pub fn number_of_beams(bank: Vec<String>) -> i32 {
    bank.into_iter()
        .filter_map(|s| {
            let c = s.bytes().filter(|&b| b == b'1').count();
            if c > 0 { Some(c as i32) } else { None }
        })
        .collect::<Vec<_>>()
        .windows(2)
        .map(|w| w[0] * w[1])
        .sum()
}

