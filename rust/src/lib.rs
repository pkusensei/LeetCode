pub fn maximum_energy(energy: Vec<i32>, k: i32) -> i32 {
    let n = energy.len();
    let k = k as usize;
    let mut dp = vec![0; n];
    let mut res = i32::MIN;
    for i in 0..n {
        if i + k < n {
            dp[i + k] = dp[i + k].max(dp[i] + energy[i]);
        } else {
            res = res.max(dp[i] + energy[i]);
        }
    }
    res
}

