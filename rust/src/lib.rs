#[allow(unused_imports)]
use helper::*;

pub fn max_operations(s: String) -> i32 {
    let mut res = 0;
    let mut count = 0;
    for ch in s.as_bytes().chunk_by(|a, b| a == b).rev() {
        if ch[0] == b'0' {
            count += 1;
        } else {
            res += count * ch.len() as i32
        }
    }
    res
}

