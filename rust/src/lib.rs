pub fn count_valid_selections(nums: Vec<i32>) -> i32 {
    let sum: i32 = nums.iter().sum();
    let mut prefix = 0;
    let mut res = 0;
    for &num in nums.iter() {
        prefix += num;
        if num == 0 {
            res += if (2 * prefix).abs_diff(sum) == 1 {
                1
            } else if 2 * prefix == sum {
                2
            } else {
                0
            }
        }
    }
    res
}

