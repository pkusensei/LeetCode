pub fn minimum_swaps(nums: Vec<i32>) -> i32 {
    let mut left = 0;
    let mut right = nums.len() - 1;
    let mut res = 0;
    while left < right {
        while left < right && nums[right] == 0 {
            right -= 1;
        }
        while left < right && nums[left] != 0 {
            left += 1;
        }
        if left < right {
            res += 1;
            left += 1;
            right -= 1;
        }
    }
    res
}

