pub fn first_unique_even(nums: Vec<i32>) -> i32 {
    use itertools::Itertools;
    let map = nums.iter().copied().filter(|&v| v & 1 == 0).counts();
    for &num in nums.iter() {
        if map.get(&num).is_some_and(|&f| f == 1) {
            return num;
        }
    }
    -1
}

