pub fn num_pairs_divisible_by60(time: Vec<i32>) -> i32 {
    let mut freq = [0; 60];
    let mut res = 0;
    for &num in time.iter() {
        let rem = num % 60;
        res += freq[((60 - rem) % 60) as usize];
        freq[rem as usize] += 1;
    }
    res
}

