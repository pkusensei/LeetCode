#[allow(unused_imports)]
use helper::*;

pub fn reverse_prefix(word: String, ch: char) -> String {
    if let Some(i) = word.find(ch) {
        let mut s = word.into_bytes();
        s[..=i].reverse();
        String::from_utf8(s).unwrap()
    } else {
        word
    }
}

