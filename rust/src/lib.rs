pub fn repeated_n_times(nums: Vec<i32>) -> i32 {
    for w in nums.windows(3) {
        // [1, 1, 2, 3]
        // [1, 2, 1, 3]
        if w[0] == w[1] || w[0] == w[2] {
            return w[0];
        }
    }
    // [1, 2, 3, 1]
    return *nums.last().unwrap();
}

