pub fn number_of_substrings(s: &str) -> i32 {
    let mut prev = [-1; 3];
    let mut res = 0;
    for (idx, b) in s.bytes().enumerate() {
        prev[usize::from(b - b'a')] = idx as i32;
        res += 1 + prev.iter().min().unwrap_or(&-1);
    }
    res
}

