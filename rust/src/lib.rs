pub fn judge_circle(moves: String) -> bool {
    let [mut a, mut b] = [0, 0];
    for v in moves.bytes() {
        match v {
            b'U' => a += 1,
            b'D' => a -= 1,
            b'L' => b += 1,
            _ => b -= 1,
        }
    }
    [a, b] == [0, 0]
}

