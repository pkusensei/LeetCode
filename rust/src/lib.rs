pub fn read_binary_watch(turned_on: i32) -> Vec<String> {
    let mut res = vec![];
    for a in 0_i32..12 {
        for b in 0_i32..60 {
            if a.count_ones() + b.count_ones() == turned_on as u32 {
                res.push(format!("{}:{:02}", a, b));
            }
        }
    }
    res
}

