pub fn count_opposite_parity(nums: Vec<i32>) -> Vec<i32> {
    let mut odd = 0;
    let mut even = 0;
    let mut res = vec![];
    for &num in nums.iter().rev() {
        if num & 1 == 1 {
            odd += 1;
            res.push(even);
        } else {
            even += 1;
            res.push(odd);
        }
    }
    res.reverse();
    res
}

