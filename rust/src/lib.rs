pub fn angle_clock(hour: i32, minutes: i32) -> f64 {
    let [hour, minutes] = [hour, minutes].map(f64::from);
    // m/60 * 30
    let h = hour * 30.0 + minutes / 2.0;
    let m = minutes * 6.0;
    let diff = (h - m).abs();
    diff.min(360.0 - diff)
}

