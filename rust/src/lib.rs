#[allow(unused_imports)]
use helper::*;

pub fn kth_largest_number(mut nums: Vec<String>, k: i32) -> String {
    nums.sort_unstable_by(|a, b| b.len().cmp(&a.len()).then(b.cmp(a)));
    nums.into_iter().nth(k as usize - 1).unwrap()
}

