pub fn prefix_connected(words: Vec<String>, k: i32) -> i32 {
    use std::collections::HashMap;
    let k = k as usize;
    let mut map = HashMap::new();
    for w in words.iter() {
        if w.len() < k {
            continue;
        }
        let p = &w[..k];
        *map.entry(p).or_insert(0) += 1;
    }
    map.iter().filter(|(k, v)| **v > 1).count() as i32
}

