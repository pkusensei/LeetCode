#[allow(unused_imports)]
use helper::*;

pub fn alternating_sum(nums: Vec<i32>) -> i32 {
    let mut res = 0;
    for (i, &v) in nums.iter().enumerate() {
        if i & 1 == 0 { res += v } else { res -= v }
    }
    res
}

