#[allow(unused_imports)]
use helper::*;

pub fn minimum_difference(mut nums: Vec<i32>, k: i32) -> i32 {
    nums.sort_unstable();
    let mut res = i32::MAX;
    for (i, left) in nums.iter().enumerate() {
        let Some(right) = nums.get(i + k as usize - 1) else {
            break;
        };
        res = res.min(right - left);
    }
    res
}

