#[allow(unused_imports)]
use helper::*;

pub fn rotate(mat: &mut Vec<Vec<i32>>) {
    let n = mat.len();
    mat.reverse();
    for r in 0..n {
        for c in 0..r {
            (mat[r][c], mat[c][r]) = (mat[c][r], mat[r][c]);
        }
    }
}

