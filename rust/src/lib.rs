pub fn map_word_weights(words: Vec<String>, weights: Vec<i32>) -> String {
    words
        .iter()
        .map(|s| {
            s.bytes()
                .map(|b| weights[usize::from(b - b'a')])
                .sum::<i32>()
                % 26
        })
        .map(|v| (b'a' + 25 - v as u8) as char)
        .collect()
}

