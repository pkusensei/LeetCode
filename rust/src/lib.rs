pub fn count_permutations(complexity: Vec<i32>) -> i32 {
    if complexity[1..].iter().any(|&v| v <= complexity[0]) {
        0
    } else {
        fact(complexity.len() as i64 - 1) as i32
    }
}

const fn fact(v: i64) -> i64 {
    if v <= 1 {
        1
    } else {
        v * fact(v - 1) % 1_000_000_007
    }
}

