#[allow(unused_imports)]
use helper::*;

pub fn broken_calc(start: i32, mut target: i32) -> i32 {
    let mut res = 0;
    while start < target {
        if target & 1 == 0 {
            target /= 2;
        } else {
            target += 1;
        }
        res += 1;
    }
    res + start - target
}

