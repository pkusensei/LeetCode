#[allow(unused_imports)]
use helper::*;

pub fn maximum_possible_size(nums: Vec<i32>) -> i32 {
    let mut res = 0;
    let mut prev = 0;
    for &num in nums.iter() {
        if prev <= num {
            prev = num;
            res += 1;
        }
    }
    res
}

