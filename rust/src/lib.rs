#[allow(unused_imports)]
use helper::*;

pub fn asteroids_destroyed(mass: i32, mut asteroids: Vec<i32>) -> bool {
    let mut mass = i64::from(mass);
    asteroids.sort_unstable();
    for a in asteroids {
        let a = i64::from(a);
        if mass >= a { mass += a } else { return false }
    }
    true
}

