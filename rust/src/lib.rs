pub const fn reach_number(target: i32) -> i32 {
    let mut num = 0;
    let mut sum = 0;
    while sum < target.abs() {
        num += 1;
        sum += num;
    }
    let delta = sum - target.abs();
    if delta & 1 == 0 {
        num
    } else if num & 1 == 1 {
        1 + num
    } else {
        2 + num
    }
}

