pub const fn check_good_integer(mut n: i32) -> bool {
    let mut dsum = 0;
    let mut ssum = 0;
    while n > 0 {
        let d = n % 10;
        n /= 10;
        dsum += d;
        ssum += d.pow(2)
    }
    ssum - dsum >= 50
}

