pub fn longest_subsequence(nums: Vec<i32>) -> i32 {
    let n = nums.len();
    let xor = nums.iter().fold(0, |acc, v| acc ^ v);
    if xor > 0 {
        n as i32
    } else if nums.iter().all(|&v| v == 0) {
        0
    } else {
        n as i32 - 1
    }
}

