pub fn num_subarray_product_less_than_k(nums: Vec<i32>, k: i32) -> i32 {
    let mut left = 0;
    let mut prod = 1;
    let mut res = 0;
    for (right, num) in nums.iter().enumerate() {
        prod *= num;
        while prod >= k && left <= right {
            prod /= nums[left];
            left += 1;
        }
        res += right + 1 - left;
    }
    res as i32
}

