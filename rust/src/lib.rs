#[allow(unused_imports)]
use helper::*;

pub fn find_smallest_set_of_vertices(n: i32, edges: &[[i32; 2]]) -> Vec<i32> {
    let indegs = edges.iter().fold(vec![true; n as usize], |mut acc, e| {
        acc[e[1] as usize] = false;
        acc
    });
    (0..n).filter(|&v| indegs[v as usize]).collect()
}

