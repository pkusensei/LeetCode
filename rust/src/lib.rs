pub fn sum_divisible_by_k(nums: Vec<i32>, k: i32) -> i32 {
    use itertools::Itertools;
    nums.iter()
        .counts()
        .into_iter()
        .filter_map(|(num, f)| {
            let f = f as i32;
            if f % k == 0 { Some(f * num) } else { None }
        })
        .sum()
}

