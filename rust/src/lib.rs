#[allow(unused_imports)]
use helper::*;

pub fn create_grid(m: i32, n: i32) -> Vec<String> {
    let [m, n] = [m, n].map(|v| v as usize);
    let mut res = vec![String::from_utf8(vec![b'.'; n]).unwrap()];
    for _ in 1..m {
        let mut v = vec![b'#'; n - 1];
        v.push(b'.');
        res.push(String::from_utf8(v).unwrap());
    }
    res
}

