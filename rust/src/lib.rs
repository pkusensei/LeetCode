pub fn sum_and_multiply(mut n: i32) -> i64 {
    let mut sum = 0;
    let mut ds = vec![];
    while n > 0 {
        let d = i64::from(n % 10);
        n /= 10;
        if d > 0 {
            ds.push(d);
            sum += d;
        }
    }
    ds.iter().rev().fold(0, |acc, d| acc * 10 + d) * sum
}

