pub fn digit_frequency_score(n: i32) -> i32 {
    n.to_string()
        .bytes()
        .fold([0; 10], |mut acc, b| {
            acc[usize::from(b - b'0')] += 1;
            acc
        })
        .iter()
        .enumerate()
        .fold(0, |acc, (i, f)| acc + i as i32 * f)
}

