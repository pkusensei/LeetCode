#[allow(unused_imports)]
use helper::*;

pub fn num_water_bottles(num_bottles: i32, num_exchange: i32) -> i32 {
    let mut res = 0;
    let mut full = num_bottles;
    let mut empty = 0;
    while full > 0 {
        res += full;
        empty = full + empty;
        full = empty / num_exchange;
        empty = empty % num_exchange;
    }
    res
}

