pub fn longest_ones(nums: Vec<i32>, k: i32) -> i32 {
    let mut res = 0;
    let mut left = 0;
    let mut zeros = 0;
    for (right, &num) in nums.iter().enumerate() {
        zeros += i32::from(num == 0);
        while zeros > k {
            zeros -= i32::from(nums[left] == 0);
            left += 1;
        }
        res = res.max(1 + right - left)
    }
    res as i32
}

