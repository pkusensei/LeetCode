pub fn residue_prefixes(s: String) -> i32 {
    let mut seen = [false; 26];
    let mut distinct = 0;
    let mut res = 0;
    for (i, b) in s.bytes().enumerate() {
        let bi = usize::from(b - b'a');
        if !seen[bi] {
            distinct += 1;
        }
        seen[bi] = true;
        if distinct == (1 + i) % 3 {
            res += 1;
        }
    }
    res
}

