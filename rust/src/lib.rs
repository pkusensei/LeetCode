pub fn merge_adjacent(nums: Vec<i32>) -> Vec<i64> {
    let mut st = vec![];
    for num in nums.iter() {
        let mut num = i64::from(*num);
        while let Some(&top) = st.last()
            && top == num
        {
            num *= 2;
            st.pop();
        }
        st.push(num);
    }
    st
}

